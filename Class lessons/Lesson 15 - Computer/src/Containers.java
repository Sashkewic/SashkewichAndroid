public class Containers {
    int countContainersForVeget; // количество контейнеров для овощей
    int countContainersForFruits; // количество контейнеров для фруктов

    public int getCountContainersForVeget() {
        return countContainersForVeget;
    }
    public void setCountContainersForVeget(int countContainersForVeget) {
        this.countContainersForVeget = countContainersForVeget;
    }

    public int getCountContainersForFruits() {
        return countContainersForFruits;
    }
    public void setCountContainersForFruits(int countContainersForFruits) {
        this.countContainersForFruits = countContainersForFruits;
    }
}
